%**********************************************************************
clear all; 

i = 0;
n = 12;
m = 6;
U0 = 0.01;
L0 = 1.13;
dL = L0/50;

for L=(L0-8*dL):(dL):(1.75*L0)
  i = i + 1;

  a = L0/L;
  
  U = m/(n-m)   * U0         * (   a^n           - n/m * a^m     );

  F = m*n/(n-m) * U0/L0      * ( - a^(n+1)       + a^(m+1)       );

  K = m*n/(n-m) * U0/(L0*L0) * (   (n+1)*a^(n+2) - (m+1)*a^(m+2) );
  
  AL(i) = L;
  AU(i) = U;
  AF(i) = F;
  AK(i) = K;
end;

figure;
plot(AL,AU*10,AL,AF,'--',AL,AK/100,'-.');grid on;axis([0.85 2 -0.2 0.1]);
xlabel('L [m]');
title('Lennard-Jones : n = 12; m = 6; L0 = 1.13; U0 = 0.01');
legend('U*10 [Nm]','F [N]','K/100 [N/m]',4);

